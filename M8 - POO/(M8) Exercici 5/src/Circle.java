public class Circle {

    //ATRIBUTTES
    private int center;
    private int radius;

    //CONSTRUCTOR    
    public Circle(int center, int radius) {
        this.center = center;
        this.radius = radius;
    }

    //GETTERS AND SETTERS
    public int getCenter() {
        return center;
    }

    public void setCenter(int center) {
        this.center = center;
    }

    public int getRadius() {
        return radius;
    }

    public void setRadius(int radius) {
        this.radius = radius;
    }    
}
